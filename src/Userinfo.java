public class Userinfo {
	String name;
	String num;
	String mail;

	Userinfo() {
		this.name = name;
		this.num = num;
		this.mail = mail;

	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNum() {
		return num;
	}

	public void setNum(String num) {
		this.num = num;
	}

	public String getMail() {
		return mail;
	}

	public void setMail(String mail) {
		this.mail = mail;
	}
}
